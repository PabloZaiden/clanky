/**
 * Create initial state for a new task.
 */
export function createInitialState(id: string): TaskState {
  return {
    id,
    status: "idle",
    currentIteration: 0,
    recentIterations: [],
    logs: [],
    messages: [],
    toolCalls: [],
  };
}

/**
 * Review comment for tracking feedback on completed tasks.
 */
export interface ReviewComment {
  /** Unique identifier (UUID) */
  id: string;
  /** Task ID this comment belongs to */
  taskId: string;
  /** Which review cycle this comment was submitted in */
  reviewCycle: number;
  /** The comment text */
  commentText: string;
  /** When the comment was created (ISO timestamp) */
  createdAt: string;
  /** Status: "pending" (being worked on) or "addressed" (completed) */
  status: "pending" | "addressed";
  /** When the comment was marked as addressed (ISO timestamp, null if pending) */
  addressedAt?: string;
}
